static inline float FOC_MathLut_Wrap0ToTwoPi(float angle)
{
    while (angle >= FOC_MATH_TWO_PI)
    {
        angle -= FOC_MATH_TWO_PI;
    }
    while (angle < 0.0f)
    {
        angle += FOC_MATH_TWO_PI;
    }
    return angle;
}

